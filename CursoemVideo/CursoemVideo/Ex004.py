inv=input('Digite algo a ser investigado:')
print('O tipo digitado é: ', type(inv))
print('É numerico? ', inv.isnumeric())
print('É alfabético?', inv.isalpha())
print('So tem espaços?', inv.isspace())
print('É alfanumerico?', inv.isalnum())
print('Esta em maiusculas?', inv.isupper())
print('Esta todo em minusculas?', inv.islower())

